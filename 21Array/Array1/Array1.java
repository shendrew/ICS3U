public class Array1 {
    public static void main(String[] args) {

        // declare and assign array
        String days[] = {"Sunday", "Monday", "Tuesday", "Wednesday", "Thursday", "Friday", "Saturday"};
    
        // display elements of array
        for (int i=0; i<7; i++)
        {
            System.out.println(days[i]);
        }
    }
}
